function hasKorean(text: string): boolean {
  return /[가-힣]/.test(text);
}

export function translateFeaturePhrase(text: string): string {
  const trimmed = text.trim();
  if (!trimmed) return trimmed;
  if (!hasKorean(trimmed)) return trimmed;

  if (FEATURE_PHRASE_MAP[trimmed]) {
    return FEATURE_PHRASE_MAP[trimmed];
  }

  let result = trimmed;
  for (const [pattern, replacement] of PATTERN_REPLACEMENTS) {
    result = result.replace(pattern, replacement);
  }

  return result;
}

export function translateFeatureList(items: string[]): string[] {
  return items.map(translateFeaturePhrase).filter(Boolean);
}

export function translateFreeLimitUnit(unit: string | null): string | null {
  if (!unit?.trim()) return null;
  const trimmed = unit.trim();
  if (!hasKorean(trimmed)) return trimmed;
  return FREE_LIMIT_UNIT_MAP[trimmed] ?? trimmed;
}
